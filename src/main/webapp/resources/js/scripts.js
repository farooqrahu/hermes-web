function handleSubmit(args, dialog) {
    var jqDialog = jQuery('#' + dialog);
    if (args.validationFailed) {
        jqDialog.effect('shake', {times: 3}, 100);
    } else {
        try {
            PF(dialog).hide();
        }
        catch (err) {
            jqDialog.dialog("close");
        }
    }
}

function start() {
    PF('statusDialog').show();
}

function stop() {
    PF('statusDialog').hide();
}

